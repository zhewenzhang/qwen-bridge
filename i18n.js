let currentLang = localStorage.getItem('autoclaude_lang') ||
  (navigator.language.startsWith('zh') ? 'zh' : 'en');

function t(key) { return I18N[currentLang]?.[key] || I18N.en[key] || key; }

function switchLang(lang) {
  currentLang = lang;
  localStorage.setItem('autoclaude_lang', lang);
  updateAllText();
  updateLangToggle();
}

function updateLangToggle() {
  const btns = document.querySelectorAll('#lang-toggle');
  btns.forEach(b => { b.textContent = currentLang === 'zh' ? 'EN' : '中文'; });
}

function updateAllText() {
  document.querySelectorAll('[data-i18n]').forEach(el => {
    const text = t(el.getAttribute('data-i18n'));
    if (text) el.innerHTML = text;
  });
  document.querySelectorAll('[data-i18n-nav]').forEach(el => {
    const text = t(el.getAttribute('data-i18n-nav'));
    if (text) el.textContent = text;
  });
}

document.addEventListener('DOMContentLoaded', () => { updateAllText(); updateLangToggle(); });
